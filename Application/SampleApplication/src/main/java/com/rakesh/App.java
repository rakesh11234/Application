package com.rakesh;

/**
 * Hello world!
 *
 */
public class App {
	
	/**
	 * 
	 * @param x
	 * @param y
	 * @return
	 */
	public int addition(int x, int y) {
		return x + y;
	}
	
	/**
	 * 
	 * @param x
	 * @param y
	 * @return
	 */
	public int substraction(int x, int y) {
		return x - y;
	}

	/**
	 * 
	 * @param x
	 * @param y
	 * @return
	 */
	public int multiplication(int x, int y) {
		return x * y;
	}

	/**
	 * 
	 * @param x
	 * @param y
	 * @return
	 */
	public int division(int x, int y) {
		return x / y;
	}
	

	/**
	 * 
	 * @return
	 */
	public String helloWorld() {
		String helloWorld = "Hello " + "World";
		return helloWorld;
	}
}
